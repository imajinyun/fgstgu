#include <stdio.h>

int main(int argc, char *argv[]) {
  /**
   * 1. 在计算机内存中，整数一律采用补码的形式来存储。这意味着，当读取整数时还要采用逆向的转换，也就是将补码转换为原码；
   * 2. 负数的补码是其反码加 1；
   * 3. 运算时根据内存中的二进制位进行运算的，而不是数据的二进制形式；
   * 4. 左移时把操作数的各个二进制位全部左移若干位，高位丢弃，低位补 0；
   * 5. 右移时把操作数的各个二进制位全部右移若干位，低位丢弃，高位补 0 或 1。如果数据的最高位是 0，那么就补 0；如果最高位是 1，那么就补 1；
   *
   * i = 00000000 00000000 00000000 00000000 -> 00000101（5 的原码，即二进制形式）
   * i = 00000000 00000000 00000000 00000000 -> 00000101（5 的反码）
   * i = 00000000 00000000 00000000 00000000 -> 00000101（5 的补码）
   *
   * j = 00000000 00000000 00000000 00000000 -> 00001001（9 的原码，即二进制形式）
   * j = 00000000 00000000 00000000 00000000 -> 00001001（9 的反码）
   * j = 00000000 00000000 00000000 00000000 -> 00001001（9 的补码）
   *
   * k = 10000000 00000000 00000000 00001001 -> 00001001（-9 的原码，即二进制形式）
   * k = 11111111 11111111 11111111 11110110 -> 11110110（-9 的反码）
   * k = 11111111 11111111 11111111 11110111 -> 11110111 (-9 的补码，在内存中的存储形式)
   */
  int i = 5, j = 9, k = -9;
  printf("i = %x, j = %x, k = %x\n\n", i, j, k);

  /**
   * 5 & 9
   *
   * 5: 00000101
   * &
   * 9: 00001001
   *    00000001 = 1
   */
  printf("i & j = %d\n", i & j);

  /**
   * 5 & -9
   *
   *  5: 00000101
   *  &
   * -9: 11110111
   *     00000101 = 5
   */
  printf("i & k = %d\n\n", i & k);

  /**
   * 5 | 9
   *
   * 5: 00000101
   * |
   * 9: 00001001
   *    00001101 = 13
   */
  printf("i | j = %d\n", i | j);

  /**
   * 5 | -9
   *
   *  5: 00000101
   *  |
   * -9: 11110111
   *     11110111 补码
   *     11110110 反码
   *     10001001 = -9
   */
  printf("i | k = %d\n\n", i | k);

  /**
   * 5 ^ 9
   *
   * 5: 00000101
   * ^
   * 9: 00001001
   *    00001100 = 12
   */
  printf("i ^ j = %d\n", i ^ j);

  /**
   * 5 ^ -9
   *
   *  5: 00000101
   *  ^
   * -9: 11110111
   *     11110010 补码
   *     11110001 反码
   *     10001110 = -(8+4+2) = -14
   */
  printf("i ^ k = %d\n\n", i ^ k);

  /**
   * 5 << 2
   *
   * 00000101 << 2
   * 00010100 = 16+4 = 20
   */
  printf("i << 2 = %d\n", i << 2);

  /**
   * -9 << 2
   *
   * 11110111 << 2
   * 11011100 补码
   * 11011011 反码
   * 10100100 = -(32+4) = -36
   */
  printf("k << 2 %d\n\n", k << 2);

  /**
   * 9 >> 2
   *
   * 00001001 >> 2
   * 00000010 = 2
   */
  printf("j >> 2 = %d\n", j >> 2);

  /**
   * -9 >> 2
   *
   * 11110111 >> 2
   * 11111101 补码
   * 11111100 取反
   * 10000011 = -3
   */
  printf("k >> 2 = %d\n\n", k >> 2);

  /**
   * -1 >> 2
   *
   * 10000001 >> 2
   * 10000000 补码
   * 11111111 反码
   * 11111111 = -1
   */
  printf("-1 >> 2 = %d\n", -1 >> 2);

  /**
   * 1 >> 2
   *
   * 00000001 >> 2
   * 00000000 = 0
   */
  printf("1 >> 2 = %d\n", 1 >> 2);

  return 0;
}
